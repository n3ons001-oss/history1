let currentQuiz = null;
let currentQuizId = null;
let currentQuestion = 0;
let score = 0;
let timeLeft = 60;
let timer = null;
let startTime = null;
let totalTimeSpent = 0;
let userAnswers = [];
let soundEnabled = true;


function playCorrectSound() {
    if (!soundEnabled) return;
    
    const sound = document.getElementById('correct-sound');
    sound.currentTime = 0; 
    sound.volume = 1.0; 
    sound.play().catch(e => {
        console.log('–ù–µ —É–¥–∞–ª–æ—Å—å –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ—Å—Ç–∏ –∑–≤—É–∫:', e);
        
        soundEnabled = false;
        updateSoundButton();
    });
}

function playWrongSound() {
    if (!soundEnabled) return;
    
    const sound = document.getElementById('wrong-sound');
    sound.currentTime = 0;
    sound.volume = 0.3; 
    sound.play().catch(e => {
        console.log('–ù–µ —É–¥–∞–ª–æ—Å—å –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ—Å—Ç–∏ –∑–≤—É–∫:', e);
        soundEnabled = false;
        updateSoundButton();
    });
}


function toggleSound() {
    soundEnabled = !soundEnabled;
    updateSoundButton();
}

function updateSoundButton() {
    const soundToggle = document.getElementById('sound-toggle');
    const soundIcon = document.getElementById('sound-icon');
    
    if (soundEnabled) {
        soundIcon.textContent = 'üîä';
        soundToggle.classList.remove('muted');
    } else {
        soundIcon.textContent = 'üîá';
        soundToggle.classList.add('muted');
    }
}


function startQuiz(quizId) {
    currentQuiz = quizDatabase[quizId];
    currentQuizId = quizId;
    currentQuestion = 0;
    score = 0;
    timeLeft = 60;
    userAnswers = [];
    
    
    document.getElementById('quiz-selection').classList.remove('active');
    document.getElementById('quiz-screen').classList.add('active');
    
    
    startTimer();
    
    
    showQuestion();
}


function showQuestion() {
    if (!currentQuiz) return;
    
    const question = currentQuiz.questions[currentQuestion];
    
    
    const progress = ((currentQuestion) / currentQuiz.questions.length) * 100;
    document.getElementById('progress-fill').style.width = progress + '%';
    document.getElementById('progress-text').textContent = `–í–æ–ø—Ä–æ—Å ${currentQuestion + 1}/${currentQuiz.questions.length}`;
    
    
    document.getElementById('question-image').innerHTML = question.image;
    document.getElementById('question-text').textContent = question.question;
    
    
    const answersContainer = document.getElementById('answers-container');
    answersContainer.innerHTML = '';
    
    question.answers.forEach((answer, index) => {
        const button = document.createElement('button');
        button.className = 'answer-btn';
        button.textContent = answer;
        button.onclick = () => checkAnswer(index);
        answersContainer.appendChild(button);
    });
    
    
    document.getElementById('next-btn').style.display = 'none';
    document.getElementById('quiz-feedback').style.display = 'none';
    document.getElementById('answer-explanation').style.display = 'none';
}


function checkAnswer(selectedIndex) {
    const question = currentQuiz.questions[currentQuestion];
    const answerButtons = document.querySelectorAll('.answer-btn');
    const feedback = document.getElementById('quiz-feedback');
    const explanation = document.getElementById('answer-explanation');
    
    
    userAnswers.push({
        question: question.question,
        userAnswer: question.answers[selectedIndex],
        correctAnswer: question.answers[question.correct],
        isCorrect: selectedIndex === question.correct
    });
    
    
    answerButtons.forEach(btn => {
        btn.classList.add('disabled');
        btn.onclick = null;
    });
    
    
    if (selectedIndex === question.correct) {
        answerButtons[selectedIndex].classList.add('correct');
        feedback.textContent = '‚úÖ –ü—Ä–∞–≤–∏–ª—å–Ω–æ! +1 –±–∞–ª–ª';
        feedback.className = 'quiz-feedback correct';
        score += 1; 
        
        playCorrectSound();
    } else {
        answerButtons[selectedIndex].classList.add('incorrect');
        answerButtons[question.correct].classList.add('correct');
        feedback.textContent = '‚ùå –ù–µ–ø—Ä–∞–≤–∏–ª—å–Ω–æ!';
        feedback.className = 'quiz-feedback incorrect';
        
        playWrongSound();
    }
    
    
    explanation.textContent = question.explanation;
    explanation.style.display = 'block';
    
    
    document.getElementById('next-btn').style.display = 'block';
}


function nextQuestion() {
    currentQuestion++;
    
    if (currentQuestion < currentQuiz.questions.length) {
        showQuestion();
    } else {
        finishQuiz();
    }
}


function startTimer() {
    startTime = new Date();
    timer = setInterval(() => {
        timeLeft--;
        document.getElementById('timer').textContent = timeLeft;
        
        if (timeLeft <= 10) {
            document.getElementById('timer').classList.add('timer-warning');
        }
        
        if (timeLeft <= 0) {
            finishQuiz();
        }
    }, 1000);
}


function finishQuiz() {
    clearInterval(timer);
    
    const endTime = new Date();
    totalTimeSpent = Math.floor((endTime - startTime) / 1000);
    
    
    document.getElementById('quiz-screen').classList.remove('active');
    document.getElementById('results-screen').classList.add('active');
    
    
    const finalScore = score;
    
    
    let finalGrade = "–ù–æ–≤–∏—á–æ–∫";
    if (finalScore >= 5) finalGrade = "–û—Ç–ª–∏—á–Ω–æ!";
    else if (finalScore >= 4) finalGrade = "–•–æ—Ä–æ—à–æ";
    else if (finalScore >= 3) finalGrade = "–£–¥–æ–≤–ª–µ—Ç–≤–æ—Ä–∏—Ç–µ–ª—å–Ω–æ";
    
    
    document.getElementById('correct-answers').textContent = `${finalScore}/${currentQuiz.questions.length}`;
    document.getElementById('time-spent').textContent = `${totalTimeSpent} —Å–µ–∫`;
    document.getElementById('total-score').textContent = finalScore;
    document.getElementById('final-grade').textContent = finalGrade;
}


function restartQuiz() {
    document.getElementById('results-screen').classList.remove('active');
    document.getElementById('quiz-selection').classList.add('active');
}


function saveResults() {
    const playerClassInput = document.getElementById('player-class').value.trim().toUpperCase();
    
    
    if (playerClassInput !== '–ê' && playerClassInput !== '–ë') {
        alert('–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–µ–¥–∏—Ç–µ –ø—Ä–∞–≤–∏–ª—å–Ω—É—é –±—É–∫–≤—É –∫–ª–∞—Å—Å–∞ (–ê –∏–ª–∏ –ë)!');
        return;
    }
    
    
    const playerName = '11' + playerClassInput;
    const finalScore = parseInt(document.getElementById('total-score').textContent);
    
    
    let leaders = JSON.parse(localStorage.getItem('quizLeaders') || '[]');
    
    
    leaders.push({
        name: playerName,
        score: finalScore,
        quiz: currentQuiz.title,
        quizId: currentQuizId,
        time: totalTimeSpent,
        date: new Date().toLocaleDateString('ru-RU')
    });
    
    
    localStorage.setItem('quizLeaders', JSON.stringify(leaders));
    
    
    window.location.href = `leaders.html?quiz=${currentQuizId}`;
}


